// server.js - Arquivo de entrada √∫nico para o Render
require('dotenv').config();

console.log('üöÄ Iniciando servidor SiteHot...');
console.log('üìÅ Executando a partir de:', __dirname);
console.log('üîß Node.js vers√£o:', process.version);
console.log('üåç Ambiente:', process.env.NODE_ENV || 'development');

const fs = require('fs');
const path = require('path');
const express = require('express');
const cors = require('cors');
const helmet = require('helmet');
const compression = require('compression');
const rateLimit = require('express-rate-limit');

// Verificar vari√°veis de ambiente essenciais
const TELEGRAM_TOKEN = process.env.TELEGRAM_TOKEN;
const BASE_URL = process.env.BASE_URL;
const PORT = process.env.PORT || 3000;

if (!TELEGRAM_TOKEN) {
  console.error('‚ùå TELEGRAM_TOKEN n√£o definido!');
  process.exit(1);
}

if (!BASE_URL) {
  console.error('‚ùå BASE_URL n√£o definido!');
  process.exit(1);
}

console.log('üîê TELEGRAM_TOKEN:', TELEGRAM_TOKEN ? 'DEFINIDO' : 'N√ÉO DEFINIDO');
console.log('üåê BASE_URL:', BASE_URL);
console.log('üö™ PORT:', PORT);

// Inicializar Express
const app = express();

// Middlewares b√°sicos
app.use(helmet({
  contentSecurityPolicy: false,
}));

app.use(compression());
app.use(cors({
  origin: true,
  credentials: true
}));

// Rate limiting
const limiter = rateLimit({
  windowMs: 15 * 60 * 1000, // 15 minutos
  max: 100
});
app.use(limiter);

// Middlewares para parsing do body
app.use(express.json({ limit: '10mb' }));
app.use(express.urlencoded({ extended: true, limit: '10mb' }));

// Middleware para logging
app.use((req, res, next) => {
  console.log(`üì° ${req.method} ${req.path} - IP: ${req.ip}`);
  next();
});

// Servir arquivos est√°ticos
const publicPath = path.join(__dirname, 'public');
const webPath = path.join(__dirname, 'MODELO1/WEB');

if (fs.existsSync(webPath)) {
  app.use(express.static(webPath));
  console.log('‚úÖ Servindo arquivos est√°ticos da pasta MODELO1/WEB');
} else if (fs.existsSync(publicPath)) {
  app.use(express.static(publicPath));
  console.log('‚úÖ Servindo arquivos est√°ticos da pasta public');
}

// Vari√°veis de controle de m√≥dulos
let bot, gerarCobranca, webhookPushinPay, gerenciadorMidia;
let databaseConnected = false;
let webModuleLoaded = false;
let postgres = null;
let databasePool = null;

// Fun√ß√£o para carregar bot
function carregarBot() {
  try {
    const botPath = path.join(__dirname, 'MODELO1', 'BOT', 'bot.js');
    
    if (!fs.existsSync(botPath)) {
      console.error('‚ùå Arquivo bot.js n√£o encontrado!');
      return false;
    }

    const botModule = require('./MODELO1/BOT/bot.js');
    bot = botModule.bot;
    gerarCobranca = botModule.gerarCobranca;
    webhookPushinPay = botModule.webhookPushinPay;
    gerenciadorMidia = botModule.gerenciadorMidia;
    
    console.log('‚úÖ Bot carregado com sucesso');
    return true;
  } catch (error) {
    console.error('‚ùå Erro ao carregar bot:', error.message);
    return false;
  }
}

// Fun√ß√£o para carregar postgres
function carregarPostgres() {
  try {
    const postgresPath = path.join(__dirname, 'postgres.js');
    
    if (fs.existsSync(postgresPath)) {
      postgres = require('./postgres');
      console.log('‚úÖ M√≥dulo postgres carregado');
      return true;
    } else {
      console.log('‚ö†Ô∏è M√≥dulo postgres n√£o encontrado');
      return false;
    }
  } catch (error) {
    console.error('‚ùå Erro ao carregar postgres:', error.message);
    return false;
  }
}

// Fun√ß√£o para inicializar banco de dados
async function inicializarBanco() {
  if (!postgres) {
    console.log('‚ö†Ô∏è M√≥dulo postgres n√£o dispon√≠vel');
    return false;
  }

  try {
    console.log('üóÑÔ∏è Inicializando banco de dados...');
    databasePool = await postgres.initializeDatabase();
    
    if (databasePool) {
      databaseConnected = true;
      console.log('‚úÖ Banco de dados inicializado com sucesso');
      return true;
    } else {
      console.log('‚ùå Falha ao inicializar banco de dados');
      return false;
    }
  } catch (error) {
    console.error('‚ùå Erro ao inicializar banco:', error.message);
    return false;
  }
}

// Fun√ß√£o para carregar sistema de tokens
async function carregarSistemaTokens() {
  try {
    const tokensPath = path.join(__dirname, 'MODELO1/WEB/tokens.js');
    
    if (!fs.existsSync(tokensPath)) {
      console.log('‚ö†Ô∏è Sistema de tokens n√£o encontrado');
      return false;
    }

    delete require.cache[require.resolve('./MODELO1/WEB/tokens')];
    const tokensModule = require('./MODELO1/WEB/tokens');
    
    if (typeof tokensModule === 'function') {
      if (databasePool) {
        tokensModule(app, databasePool);
        webModuleLoaded = true;
        console.log('‚úÖ Sistema de tokens carregado com pool de conex√µes');
      } else {
        console.log('‚ö†Ô∏è Sistema de tokens n√£o carregado - pool n√£o dispon√≠vel');
      }
      return true;
    } else {
      console.log('‚ùå Sistema de tokens n√£o √© uma fun√ß√£o');
      return false;
    }
  } catch (error) {
    console.error('‚ùå Erro ao carregar sistema de tokens:', error.message);
    return false;
  }
}

// Configurar webhook do Telegram
const webhookPath = `/bot${TELEGRAM_TOKEN}`;
console.log('üîó Configurando webhook no caminho:', webhookPath);

app.post(webhookPath, (req, res) => {
  try {
    console.log('üì® Webhook do Telegram recebido');
    
    if (!bot) {
      console.error('‚ùå Bot n√£o inicializado');
      return res.status(500).json({ error: 'Bot n√£o inicializado' });
    }
    
    bot.processUpdate(req.body);
    res.sendStatus(200);
    
  } catch (error) {
    console.error('‚ùå Erro ao processar webhook do Telegram:', error);
    res.status(500).json({ error: 'Erro interno do servidor' });
  }
});

// Webhook do PushinPay
app.post('/webhook/pushinpay', async (req, res) => {
  try {
    console.log('üí∞ Webhook PushinPay recebido');
    
    if (!webhookPushinPay) {
      console.error('‚ùå Fun√ß√£o webhookPushinPay n√£o dispon√≠vel');
      return res.status(500).json({ error: 'Webhook handler n√£o dispon√≠vel' });
    }
    
    await webhookPushinPay(req, res);
    
  } catch (error) {
    console.error('‚ùå Erro no webhook PushinPay:', error);
    res.status(500).json({ error: 'Erro interno do servidor' });
  }
});

// API para gerar cobran√ßa
app.post('/api/gerar-cobranca', async (req, res) => {
  try {
    console.log('üí≥ API gerar cobran√ßa chamada');
    
    if (!gerarCobranca) {
      console.error('‚ùå Fun√ß√£o gerarCobranca n√£o dispon√≠vel');
      return res.status(500).json({ error: 'Fun√ß√£o de cobran√ßa n√£o dispon√≠vel' });
    }
    
    await gerarCobranca(req, res);
    
  } catch (error) {
    console.error('‚ùå Erro na API de cobran√ßa:', error);
    res.status(500).json({ error: 'Erro interno do servidor' });
  }
});

