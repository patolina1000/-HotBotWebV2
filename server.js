// Graceful shutdown
process.on('SIGTERM', () => {
  console.log('üîÑ Recebido SIGTERM, encerrando servidor...');
  
  if (databasePool && postgres) {
    databasePool.end().then(() => {
      console.log('üóÑÔ∏è Pool de conex√µes fechado');
    }).catch(err => {
      console.error('‚ùå Erro ao fechar pool:', err);
    });
  }
  
  server.close(() => {
    console.log('‚úÖ Servidor fechado');
    process.exit(0);
  });
});

process.on('SIGINT', () => {
  console.log('üîÑ Recebido SIGINT, encerrando servidor...');
  
  if (databasePool && postgres) {
    databasePool.end().then(() => {
      console.log('üóÑÔ∏è Pool de conex√µes fechado');
    }).catch(err => {
      console.error('‚ùå Erro ao fechar pool:', err);
    });
  }
  
  server.close(() => {
    console.log('‚úÖ Servidor fechado');
    process.exit(0);
  });
});

