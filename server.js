// Inicializar m√≥dulos ap√≥s configurar rotas
async function inicializarModulos() {
  console.log('\nüöÄ Inicializando m√≥dulos...');
  
  // 1. Carregar bot
  const botCarregado = carregarBot();
  if (botCarregado) {
    console.log('‚úÖ Bot inicializado');
  } else {
    console.log('‚ö†Ô∏è Bot n√£o inicializado');
  }
  
  // 2. Carregar postgres
  const postgresCarregado = carregarPostgres();
  
  // 3. Inicializar banco se postgres est√° dispon√≠vel
  if (postgresCarregado) {
    await inicializarBanco();
  }
  
  // 4. Carregar sistema de tokens
  await carregarSistemaTokens();
  
  console.log('\nüìä Status dos m√≥dulos:');
  console.log(`ü§ñ Bot: ${bot ? 'OK' : 'ERRO'}`);
  console.log(`üóÑÔ∏è Banco: ${databaseConnected ? 'OK' : 'ERRO'}`);
  console.log(`üéØ Tokens: ${webModuleLoaded ? 'OK' : 'ERRO'}`);
}

